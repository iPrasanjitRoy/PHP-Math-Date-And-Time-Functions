<?php
$number = 10.5;
$roundedUp = ceil($number);
echo "Ceiling Value: $roundedUp\n";

$number = 10.5;
$roundedDown = floor($number);
echo "Floor Value: $roundedDown\n";

$number = 7.5;
$rounded = round($number);
echo "Rounded Value: $rounded\n";

$number = -7;
$absoluteValue = abs($number);
echo "Absolute Value: $absoluteValue\n";
?>